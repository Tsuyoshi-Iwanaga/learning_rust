fn main() {
  //タプル 複数の異なる型をまとめることができる、一度長さを決めると変更できない
  let t1: (i32, f64, u8) = (50, 0.8, 12);
  let t2 = (123, 2.3, 10);

  println!("{}, {}, {}", t1.0, t1.1, t1.2);
  println!("{}, {}, {}", t2.0, t2.1, t2.2);

  //タプルから新しいタプルを作る
  let (x, y, z) = t2;
  println!("{}, {}, {}", x, y, z);

  //全体を出力する
  println!("{:?}", t1);

  //mutなら同じデータ型であれば中身を変更可能
  let mut tm = (12, 24);
  println!("{:?}", tm);
  tm.0 = 23;
  println!("{:?}", tm);
}